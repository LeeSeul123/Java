package com.yedam.subject;

public class Subject {
	private int subjectId;
	private String subjectName;
	private int subjectStartTime;
	private int subjectEndTime;
	private int subjectWeek;
	private String professorId;
	
	public int getSubjectId() {
		return subjectId;
	}
	public void setSubjectId(int subjectId) {
		this.subjectId = subjectId;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public int getSubjectStartTime() {
		return subjectStartTime;
	}
	public void setSubjectStartTime(int subjectStartTime) {
		this.subjectStartTime = subjectStartTime;
	}
	public int getSubjectEndTime() {
		return subjectEndTime;
	}
	public void setSubjectEndTime(int subjectEndTime) {
		this.subjectEndTime = subjectEndTime;
	}
	public int getSubjectWeek() {
		return subjectWeek;
	}
	public void setSubjectWeek(int subjectWeek) {
		this.subjectWeek = subjectWeek;
	}
	public String getProfessorId() {
		return professorId;
	}
	public void setProfessorId(String professorId) {
		this.professorId = professorId;
	}
	
	
}
